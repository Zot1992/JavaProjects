package com.tfkfan.gui;

public class Arr_points {

    private double X=0;
    private double Y=0;

    public Arr_points(){}

    public Arr_points(double X,double Y){
        this.X=X;
        this.Y=Y;

    }

    public double getX() {
        return X;
    }

    public double getY() {
        return Y;
    }

}
